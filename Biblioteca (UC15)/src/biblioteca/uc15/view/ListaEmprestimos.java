    private void preencherTabelaLivro() {
        BiblioDAO biblioDAO = new BiblioDAO();
       
        String livroEmprestimo = txtFiltroLivro.getText();
        List<Emprestimo> listaEmprestimos = biblioDAO.getEmprestimosLivro(livroEmprestimo);
        
        DefaultTableModel model = (DefaultTableModel) tblEmprestimos.getModel();
        model.setRowCount(0); 

        DefaultTableModel tabelaLivros = (DefaultTableModel) tblEmprestimos.getModel();
        tabelaLivros.setNumRows(0);

    
        tblEmprestimos.setRowSorter(new TableRowSorter<>(tblEmprestimos.getModel()));

        if (listaEmprestimos != null) {
            for (Emprestimo c : listaEmprestimos) {
                Object[] obj = new Object[] {
                    c.getId(),              
                    c.getUsuario(),
                    c.getLivro(),
                    c.getDataEmprestimo(),
                    c.getDataPrevista()
                };
            tabelaLivros.addRow(obj);
            }
        }
    }
    
