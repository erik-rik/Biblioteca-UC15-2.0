    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int selectedRow = tblEmprestimos.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um empréstimo para excluir.");
            return;
        }

        int idEmprestimo = (int) tblEmprestimos.getValueAt(selectedRow, 0);

        int confirm = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este empréstimo?", "Confirmação", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            BiblioDAO biblioDAO = new BiblioDAO();
            boolean sucesso = biblioDAO.excluirEmprestimo(idEmprestimo);

            if (sucesso) {
                JOptionPane.showMessageDialog(this, "Empréstimo excluído com sucesso!");
                preencherTabela();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir empréstimo.");
            }
        }    
    }//GEN-LAST:event_btnExcluirActionPerformed

